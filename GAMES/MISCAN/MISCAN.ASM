       INC CANA
       BRA F5
       SPC 1
ODDCAN TST CANA
       BNE CANANZ
       BRA ERRORR NO CANNIBAL A, ERROR
       SPC 1
CANANZ DEC CANA MOVE RIGHT
       INC CANB
F5     DEC COUNT2
       BNE LOOP
