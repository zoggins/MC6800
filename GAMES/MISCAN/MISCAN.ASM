       INC MISB
       BRA F5
       SPC 1
F3     TST TRIP MOVE CANNIBAL
       BNE ODDCAN
       TST CANB
       BNE CANBNZ
       BRA ERRORR NO CANNIBAL B, ERROR
       SPC 1
