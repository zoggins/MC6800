       DEC COUNT1
       BNE SLOOP
       LDAA CANA PRNT CANA VALUE
       BSR ONC
       LDAA #'C
       BSR OC2S
       LDAA MISA PRINT MISA VALUE
       BSR ONC
       LDAA #'M
       BSR OC2S
       LDAA #16
       STAA COUNT1
F6     TST TRIP
       BNE RGHTPT
       LDAA #'<
       BSR OUTCHR
       BRA F7
       SPC 1
OUTCHR JMP OUTCH
       SPC 1
RGHTPT LDAA #'>
       BSR OUTCHR
F7     DEC COUNT1
       BNE F6
       BSR P2SP
       LDAA CANB PRINT CANB VALUE
       BSR ONC
       LDAA #'C
       BSR OC2S
       LDAA MISB PRINT MISB VALUE
       BSR ONC
       LDAA #'M
       BSR OUTCHR
       TST MISA MISA AND CANA =  0???
       BNE F9
       TST CANA
       BNE F9
       BRA CONGTR YES WIN
       SPC 1
F9     LDAA CANA IS CANA > MISA?
       CMPA MISA
       BLE F10
       TST MISA MISA NOT 0?
       BEQ F10
       BRA BURN YES LOSE
       SPC 1
F10    LDAA CANB IS CANB > MISB
       CMPA MISB
       BLE F11
       TST MISB MISB NOT 0?
       BEQ F11
       BRA BURN YES LOSE
       SPC 1
F11    COM TRIP COMPLEMENT TRIP FLAG
       JMP CONTIN
       SPC 3
